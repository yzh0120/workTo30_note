/*
在后台管理的系统中经常有很多表格和表单
而且一个表单里面经常会有很多控件
为了方便开发和维护,根据vue内置的component组件就封装了表单组件
它接收一个数组,数组里面是一个个表单对象,表单组件内部会循环这个数组
如果对象的type是input,那么就将component组件渲染成input组件
如果type是select 那又会渲染成select组件


有一个给国企做了一个非融资系统
快结束的时候,对方提了一个需求是设计到财务的地方的输入框都需要千分位显示,但是传给后台肯定还是正常的数字
这次改动就设计到很多地方了,如果是用原生的elinput写就需要每一个input都需要处理
但是用封装的表单组件写只需要在内部的input处理好就行
*/ 